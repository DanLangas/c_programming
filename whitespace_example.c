#include <stdio.h>

/* whitespace separates one part of a statement from another
 * and enables the compiler to identify where one element in 
 * a statement ends and the next element begins*/

int age; // must be at least one whitespace between int and age for the compiler to be able to distinguish them

fruit = apples + oranges; // no whitespace characters are necessary between fruit and =, or  between = and apples, but this increase readability

